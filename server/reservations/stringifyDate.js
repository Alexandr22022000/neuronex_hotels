module.exports = date => {
    const days = [
        "Воскресенье",
        "Понедельник",
        "Вторник",
        "Среда",
        "Четверг",
        "Пятница",
        "Суббота",
    ];

    const monts = [
        "января",
        "февраля",
        "марта",
        "апреля",
        "мая",
        "июня",
        "июля",
        "августа",
        "сентября",
        "октября",
        "ноября",
        "декабря",
    ];

    date = new Date(date);
    return days[date.getDay()] + ", " + date.getDate() + " " + monts[date.getMonth()] + " " + date.getFullYear();
};